#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;


	p = (char*)mmap(NULL, (ft_strlen(s1) * sizeof(char) + 1), FLAG_MALLOC, -1, 0);
	if (p != NULL)
		ft_strcpy(p, s1);
	return (p);
}
